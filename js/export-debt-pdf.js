/* ============================================================
   FinTrack — Debt PDF Export
   Builds a one-page PDF for a single debt: person, amounts,
   due date, and full payment history. Download or share via
   the native share sheet (WhatsApp, email, etc.).
   ============================================================ */

function buildDebtDoc(debt) {
  const { jsPDF } = window.jspdf;
  const doc = new jsPDF();
  const sym = debt.wallet === 'aed' ? 'AED ' : 'Rs. ';

  // Header
  doc.setFillColor(13, 19, 33);
  doc.rect(0, 0, 210, 28, 'F');
  doc.setTextColor(255, 255, 255);
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(18);
  doc.text('FinTrack', 14, 14);
  doc.setFontSize(9);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(212, 175, 55);
  doc.text('Track. Control. Grow.', 14, 21);

  let y = 40;
  doc.setTextColor(20, 20, 20);
  doc.setFontSize(14);
  doc.setFont('helvetica', 'bold');
  doc.text(debt.direction === 'i_owe' ? 'Debt — You Owe' : 'Debt — Owed to You', 14, y);
  y += 6;
  doc.setFontSize(9);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(100, 100, 100);
  doc.text(`Generated ${new Date().toLocaleDateString('en-GB', { day: 'numeric', month: 'long', year: 'numeric' })}`, 14, y);

  // Person + summary box
  y += 12;
  doc.setDrawColor(220, 220, 220);
  doc.setFillColor(245, 245, 245);
  doc.roundedRect(14, y, 182, 42, 2, 2, 'F');

  doc.setFontSize(9);
  doc.setTextColor(120, 120, 120);
  doc.text('PERSON', 20, y + 10);
  doc.setFontSize(13);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(20, 20, 20);
  doc.text(debt.person, 20, y + 18);

  doc.setFontSize(9);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(120, 120, 120);
  doc.text('TOTAL', 20, y + 30);
  doc.text('REMAINING', 90, y + 30);
  doc.text('DUE DATE', 150, y + 30);

  doc.setFontSize(11);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(20, 20, 20);
  doc.text(`${sym}${debt.total.toLocaleString('en-IN')}`, 20, y + 37);
  doc.setTextColor(debt.remaining > 0 ? 192 : 46, debt.remaining > 0 ? 91 : 125, debt.remaining > 0 ? 77 : 107);
  doc.text(`${sym}${debt.remaining.toLocaleString('en-IN')}`, 90, y + 37);
  doc.setTextColor(20, 20, 20);
  doc.text(debt.due ? new Date(debt.due).toLocaleDateString('en-GB', { day: 'numeric', month: 'short', year: 'numeric' }) : '—', 150, y + 37);

  y += 52;
  if (debt.notes) {
    doc.setFontSize(9);
    doc.setTextColor(100, 100, 100);
    doc.text(`Notes: ${debt.notes}`, 14, y);
    y += 10;
  }

  // Payment history
  doc.setFontSize(11);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(20, 20, 20);
  doc.text('Payment History', 14, y);
  y += 6;

  if (!debt.payments || debt.payments.length === 0) {
    doc.setFontSize(9);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(120, 120, 120);
    doc.text('No payments logged yet.', 14, y + 4);
  } else {
    doc.setFontSize(9);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(120, 120, 120);
    doc.text('Date', 14, y);
    doc.text('Amount', 130, y);
    y += 4;
    doc.setDrawColor(220, 220, 220);
    doc.line(14, y, 196, y);
    y += 5;

    doc.setFont('helvetica', 'normal');
    debt.payments.slice().reverse().forEach(p => {
      const psym = (p.currency || debt.wallet) === 'aed' ? 'AED ' : 'Rs. ';
      doc.setTextColor(40, 40, 40);
      doc.text(new Date(p.date).toLocaleDateString('en-GB', { day: 'numeric', month: 'short', year: 'numeric' }), 14, y);
      doc.setTextColor(46, 125, 107);
      doc.text(`+${psym}${p.amount.toLocaleString('en-IN')}`, 130, y);
      y += 6;
    });
  }

  doc.setFontSize(8);
  doc.setTextColor(160, 160, 160);
  doc.text('Generated by FinTrack', 14, 287);

  return doc;
}

function downloadDebtPDF(debt) {
  const doc = buildDebtDoc(debt);
  const filename = `debt-${debt.person.replace(/\s+/g, '-').toLowerCase()}-${new Date().toISOString().slice(0, 10)}.pdf`;
  doc.save(filename);
}

async function shareDebtPDF(debt) {
  const doc = buildDebtDoc(debt);
  const filename = `debt-${debt.person.replace(/\s+/g, '-').toLowerCase()}.pdf`;
  const blob = doc.output('blob');
  const file = new File([blob], filename, { type: 'application/pdf' });

  if (navigator.canShare && navigator.canShare({ files: [file] })) {
    try {
      await navigator.share({ files: [file], title: 'FinTrack Debt Details', text: `Debt details for ${debt.person}` });
    } catch (err) {
      // User cancelled — no action needed
    }
  } else {
    doc.save(filename);
    alert("Sharing isn't supported on this browser — the PDF was downloaded instead.");
  }
}
