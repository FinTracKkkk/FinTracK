/* ============================================================
   FinTrack — Split Expense PDF Export
   Individual split PDFs (from Expense History) and a full report
   across all splits for the current wallet filter.
   ============================================================ */

function pdfHeader(doc, subtitle) {
  doc.setFillColor(13, 19, 33);
  doc.rect(0, 0, 210, 28, 'F');
  doc.setTextColor(255, 255, 255);
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(18);
  doc.text('FinTrack', 14, 14);
  doc.setFontSize(9);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(212, 175, 55);
  doc.text('Track. Control. Grow.', 14, 21);

  doc.setTextColor(20, 20, 20);
  doc.setFontSize(13);
  doc.setFont('helvetica', 'bold');
  doc.text(subtitle, 14, 40);
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(9);
  doc.setTextColor(100, 100, 100);
  doc.text(`Generated ${new Date().toLocaleDateString('en-GB', { day: 'numeric', month: 'long', year: 'numeric' })}`, 14, 46);
}

/* ---------- Individual split ---------- */
function buildSplitDoc(split) {
  const { jsPDF } = window.jspdf;
  const doc = new jsPDF();
  const sym = split.wallet === 'aed' ? 'AED ' : 'Rs. ';

  pdfHeader(doc, split.title);
  let y = 56;

  doc.setFontSize(9);
  doc.setTextColor(100, 100, 100);
  doc.text(`Date: ${new Date(split.date).toLocaleDateString('en-GB', { day: 'numeric', month: 'short', year: 'numeric' })}`, 14, y);
  y += 10;

  doc.setDrawColor(220, 220, 220);
  doc.setFillColor(245, 245, 245);
  doc.roundedRect(14, y, 182, 30, 2, 2, 'F');
  doc.setFontSize(9);
  doc.setTextColor(120, 120, 120);
  doc.text('TOTAL', 20, y + 10);
  doc.text('COLLECTED', 90, y + 10);
  doc.text('OUTSTANDING', 150, y + 10);
  doc.setFontSize(12);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(20, 20, 20);
  doc.text(`${sym}${split.totalAmount.toLocaleString('en-IN')}`, 20, y + 20);
  doc.setTextColor(46, 125, 107);
  doc.text(`${sym}${split.collected.toLocaleString('en-IN')}`, 90, y + 20);
  doc.setTextColor(192, 91, 77);
  doc.text(`${sym}${split.outstanding.toLocaleString('en-IN')}`, 150, y + 20);

  y += 40;
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(11);
  doc.setTextColor(20, 20, 20);
  doc.text('Split Between', 14, y);
  y += 6;
  doc.setFontSize(9);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(120, 120, 120);
  doc.text('Friend', 14, y);
  doc.text('Share', 130, y);
  y += 4;
  doc.setDrawColor(220, 220, 220);
  doc.line(14, y, 196, y);
  y += 5;

  doc.setFont('helvetica', 'normal');
  split.friends.forEach(f => {
    doc.setTextColor(40, 40, 40);
    doc.text(f.name, 14, y);
    doc.text(`${sym}${f.share.toLocaleString('en-IN')}`, 130, y);
    y += 6;
  });

  if (split.notes) {
    y += 6;
    doc.setFontSize(9);
    doc.setTextColor(100, 100, 100);
    doc.text(`Notes: ${split.notes}`, 14, y);
  }

  doc.setFontSize(8);
  doc.setTextColor(160, 160, 160);
  doc.text('Generated by FinTrack', 14, 287);

  return doc;
}

function downloadSplitPDF(split) {
  const doc = buildSplitDoc(split);
  doc.save(`split-${split.title.replace(/\s+/g, '-').toLowerCase()}-${split.date}.pdf`);
}

async function shareSplitPDF(split) {
  const doc = buildSplitDoc(split);
  const filename = `split-${split.title.replace(/\s+/g, '-').toLowerCase()}.pdf`;
  const blob = doc.output('blob');
  const file = new File([blob], filename, { type: 'application/pdf' });

  if (navigator.canShare && navigator.canShare({ files: [file] })) {
    try {
      await navigator.share({ files: [file], title: 'FinTrack Split Expense', text: `${split.title} — split expense details` });
    } catch (err) { /* cancelled */ }
  } else {
    doc.save(filename);
    alert("Sharing isn't supported on this browser — the PDF was downloaded instead.");
  }
}

/* ---------- Full report (all splits + friend balances) for current wallet ---------- */
function buildAllSplitsDoc(wallet) {
  const { jsPDF } = window.jspdf;
  const doc = new jsPDF();
  const sym = wallet === 'aed' ? 'AED ' : 'Rs. ';
  const walletLabel = wallet === 'aed' ? 'Dubai (AED)' : 'India (INR)';

  pdfHeader(doc, `Split Expense Report — ${walletLabel}`);
  let y = 56;

  const stats = splitDashboardStats(wallet);
  doc.setDrawColor(220, 220, 220);
  doc.setFillColor(245, 245, 245);
  doc.roundedRect(14, y, 182, 30, 2, 2, 'F');
  doc.setFontSize(9);
  doc.setTextColor(120, 120, 120);
  doc.text('TOTAL EXPENSES', 20, y + 10);
  doc.text('OUTSTANDING', 90, y + 10);
  doc.text('COLLECTED', 150, y + 10);
  doc.setFontSize(12);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(20, 20, 20);
  doc.text(`${sym}${stats.totalExpenses.toLocaleString('en-IN')}`, 20, y + 20);
  doc.setTextColor(192, 91, 77);
  doc.text(`${sym}${stats.totalOutstanding.toLocaleString('en-IN')}`, 90, y + 20);
  doc.setTextColor(46, 125, 107);
  doc.text(`${sym}${stats.totalCollected.toLocaleString('en-IN')}`, 150, y + 20);

  y += 40;
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(11);
  doc.setTextColor(20, 20, 20);
  doc.text('Friend Balances', 14, y);
  y += 6;

  const friends = getFriendBalances(wallet);
  if (friends.length === 0) {
    doc.setFontSize(9);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(120, 120, 120);
    doc.text('No split balances yet.', 14, y + 4);
    y += 10;
  } else {
    doc.setFontSize(9);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(120, 120, 120);
    doc.text('Name', 14, y);
    doc.text('Remaining', 130, y);
    y += 4;
    doc.setDrawColor(220, 220, 220);
    doc.line(14, y, 196, y);
    y += 5;
    doc.setFont('helvetica', 'normal');
    friends.forEach(f => {
      if (y > 270) { doc.addPage(); y = 20; }
      doc.setTextColor(40, 40, 40);
      doc.text(f.name, 14, y);
      doc.setTextColor(f.remaining > 0 ? 192 : 46, f.remaining > 0 ? 91 : 125, f.remaining > 0 ? 77 : 107);
      doc.text(`${sym}${f.remaining.toLocaleString('en-IN')}`, 130, y);
      y += 6;
    });
    y += 6;
  }

  // Expense history
  if (y > 250) { doc.addPage(); y = 20; }
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(11);
  doc.setTextColor(20, 20, 20);
  doc.text('Expense History', 14, y);
  y += 6;

  const expenses = getExpenseHistory(wallet);
  if (expenses.length === 0) {
    doc.setFontSize(9);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(120, 120, 120);
    doc.text('No expenses logged yet.', 14, y + 4);
  } else {
    doc.setFontSize(9);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(120, 120, 120);
    doc.text('Title', 14, y);
    doc.text('Total', 110, y);
    doc.text('Status', 160, y);
    y += 4;
    doc.setDrawColor(220, 220, 220);
    doc.line(14, y, 196, y);
    y += 5;
    doc.setFont('helvetica', 'normal');
    expenses.forEach(e => {
      if (y > 270) { doc.addPage(); y = 20; }
      doc.setTextColor(40, 40, 40);
      doc.text(e.title, 14, y);
      doc.text(`${sym}${e.totalAmount.toLocaleString('en-IN')}`, 110, y);
      doc.setTextColor(e.status === 'settled' ? 46 : e.status === 'partial' ? 180 : 100, e.status === 'settled' ? 125 : e.status === 'partial' ? 140 : 100, e.status === 'settled' ? 107 : 40);
      doc.text(e.status, 160, y);
      y += 6;
    });
  }

  doc.setFontSize(8);
  doc.setTextColor(160, 160, 160);
  doc.text('Generated by FinTrack', 14, 287);

  return doc;
}

function downloadAllSplitsPDF(wallet) {
  const doc = buildAllSplitsDoc(wallet);
  doc.save(`split-expense-report-${wallet}-${new Date().toISOString().slice(0, 10)}.pdf`);
}

async function shareAllSplitsPDF(wallet) {
  const doc = buildAllSplitsDoc(wallet);
  const filename = `split-expense-report-${wallet}.pdf`;
  const blob = doc.output('blob');
  const file = new File([blob], filename, { type: 'application/pdf' });

  if (navigator.canShare && navigator.canShare({ files: [file] })) {
    try {
      await navigator.share({ files: [file], title: 'FinTrack Split Expense Report' });
    } catch (err) { /* cancelled */ }
  } else {
    doc.save(filename);
    alert("Sharing isn't supported on this browser — the PDF was downloaded instead.");
  }
}
